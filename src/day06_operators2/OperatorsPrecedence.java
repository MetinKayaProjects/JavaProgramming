package day06_operators2;

public class OperatorsPrecedence {
	public static void main(String[] args) {
		System.out.println("Hello " + 6 + 10); //Hello 610 yazdi?! Soldan saga hesapliyor
		
		System.out.println("Hello " + 10 + 6); //Hello 106
		
		System.out.println(6+10+ " Hello"); //16 Hello
		
		System.out.println("Hello " + (6 + 10)); //parantez icini once hesaplar...
		
		
		
		
	}

}
