package day23_array_Vasyl;

public class Triangle {
	public static void main(String[] args) {
		
		
		for(int j =1; j <=10; j++) {
			for(int i = 1; i <=j; i++) {
				System.out.print(i + " ");
			}
			System.out.println();
		}
		
		
		//in three separate line and three times print out the numbers from 1 to 10 
		System.out.println();
		
		for(int k=1; k<=3; k++) {
			for(int m = 1; m <= 10; m++) {
			System.out.print(m + " ");
			}
			System.out.println();
		}
		
	}

}
