package day18_while_dowhile_loops;

public class Zero_Hundred_077 {
	public static void main(String[] args) {
		
//		use a while loop to print numbers from 0 to 100
//
//		example:
//		0
//		1
//		2
//	   ...
//		99
//		100	
	
		
		int i = 0;
		while(i <= 100) {
			System.out.println(i);
			i++;
		}
		System.out.println("Counter: " + i);
	}

}
