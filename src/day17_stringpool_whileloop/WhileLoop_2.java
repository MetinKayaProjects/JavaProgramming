package day17_stringpool_whileloop;



public class WhileLoop_2 {
	public static void main(String[] args) {
		
		int x = 1;
		while(x <= 5) {
			
			System.out.println("Happy new year" + x);
			++x;  // Add 1 to x
		}
		
		
		System.out.println("\n**********************\n");
		
		int y = 2;
		while(y < 5) {
			System.out.println(y+")" + "Java is fun");
			++y;
			
		}
		
	}

}
